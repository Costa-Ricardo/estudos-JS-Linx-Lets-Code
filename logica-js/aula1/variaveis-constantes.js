// TIPOS PRIMITIVOS COM VAR

var numero = 1;
var texto = 'Texto';
var booleano = true;
var undefined;
var nulo = null; 

console.log('Console: ',numero);
console.log('Console: ', texto);
console.log('Console: ', booleano);
console.log('Console: ', undefined);
console.log('Console: ', nulo)

// TIPOS PRIMITIVOS COM LET
let numero1 = 2;
let texto1 = 'Escrita';
let booleano1 = false;
let undefined1; 
let nulo1 = null;

console.log('Console1: ',numero1);
console.log('Console1: ', texto1);
console.log('Console1: ', booleano1);
console.log('Console1: ', undefined1);
console.log('Console1: ', nulo1)

